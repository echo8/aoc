    public static Set<Long> getCornerTileIds() {
        List<Tile> tiles = new ArrayList<>();

        String[] rawTiles = INPUT.split("\n\n");
        for (int i = 0; i < rawTiles.length; i++) {
            String[] lines = rawTiles[i].split("\n");
            long id = Long.parseLong(lines[0].split(" ")[1].replace(":", ""));
            List<String> sides = new ArrayList<>();
            StringBuilder leftSb = new StringBuilder();
            StringBuilder rightSb = new StringBuilder();
            for (int j = 1; j < lines.length; j++) {
                if (j == 1) {
                    sides.add(lines[j]);
                } else if (j == lines.length-1) {
                    sides.add(lines[j]);
                }
                leftSb.append(lines[j].charAt(0));
                rightSb.append(lines[j].charAt(lines[j].length()-1));
            }
            sides.add(leftSb.toString());
            sides.add(rightSb.toString());
            tiles.add(new Tile(id, sides));
        }
//        System.out.println(tiles);

        Map<String, Set<Tile>> sideMap = new HashMap<>();
        for (Tile tile : tiles) {
            for (String side : tile.sides) {
                if (!sideMap.containsKey(side)) {
                    sideMap.put(side, new HashSet<>());
                }
                sideMap.get(side).add(tile);
                String reversedSide = StringUtils.reverse(side);
                if (!sideMap.containsKey(reversedSide)) {
                    sideMap.put(reversedSide, new HashSet<>());
                }
                sideMap.get(reversedSide).add(tile);
            }
        }
//        System.out.println(sideMap);

        Set<Long> cornerIds = new HashSet<>();
        long res = 1;
        int cnt = 0;
        for (Tile tile : tiles) {
            int sameCount = 0;
            for (String side : tile.sides) {
                int c = sideMap.get(side).size();
                int rc = sideMap.get(StringUtils.reverse(side)).size();
                if (c == 1 && rc == 1) {
                    sameCount++;
                }
            }
            if (sameCount == 2) {
                cornerIds.add(tile.id);
                res = res * tile.id;
                cnt++;
            } else {
//                System.out.println("id=" + tile.id + " sameCount=" + sameCount);
            }
        }
        System.out.println(res);
        System.out.println(cnt);
        return cornerIds;
    }
