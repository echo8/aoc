    private static class Tile {
        long id;
        List<String> sides;

        public Tile(long id, List<String> sides) {
            this.id = id;
            this.sides = sides;
        }

        @Override
        public boolean equals(Object o) {
            if (this == o) return true;
            if (o == null || getClass() != o.getClass()) return false;
            Tile tile = (Tile) o;
            return id == tile.id;
        }

        @Override
        public int hashCode() {
            return Objects.hash(id);
        }

        @Override
        public String toString() {
            return "Tile{" +
                    "id=" + id +
                    ", sides=" + sides +
                    '}';
        }
    }

